                            <span class="accordion__question_icon question-bg"></span>
                            Можно ли с собой взять еду и напитки?
                        </span>
                    </div>
                    <div class="accordion__a">
<p>Однажды решив  воспользоваться нашей услугой аренды яхт, вы однозначно получите одни из самых незабываемых впечатлений в вашей жизни! Однако, чтоб это, наполненное эмоциями времяпрепровождения, не было заполнено мыслями о какой-нибудь «вкусняшке», которая Вас ждёт на берегу или же охлаждающих/согревающих напитках, которых так не хватает в море, то необходимо заранее позаботиться о еде и напитках на время выхода в море!
</p>
<p class="faq__bold">Еду и напитки можно и нужно взять с собой!
</p>
<p>Обязательно возьмите запас питьевой воды ( в расчёте 1-1,5 литра  на человека). Даже если вы арендуете яхту на короткий промежуток времени, не забывайте, что вода - наше всё!  
</p>
<p>Здесь действует принцип : «Лучше больше, чем не хватит!»
</p>
<p>Из продовольствия , мы советуем взять  уже готовую еду, для того, чтоб не тратить драгоценное время отдыха на приготовление закусок и бутербродов. Это могут быть  овощи, фрукты, готовые сырные/мясные нарезки, печенье.  Учитывайте обязательно время года, когда вы арендуете яхту. В жаркие, знойные летние дни не берите скоропортящиеся продукты, мороженое, шоколад.
</p>
<p>В прохладное время года не забудьте захватить с собой согревающие напитки (чай/кофе).  Ведь что может быть прекраснее, чем налить чашечку горячего, бодрящего,  крепкого кофе, укутаться пледом и с каждым новым глотком  согреваться телом и  ощущать себя счастливым душой,находясь на борту прекрасной яхты в открытом море?
</p>
