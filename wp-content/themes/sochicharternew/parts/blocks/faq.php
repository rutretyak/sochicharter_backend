                    <div class="accordion__a">
<p>Взять яхту в аренду в настоящее время  не составляет особого труда.  Арендуя яхту, вы должны помнить и знать, что на борту может находиться до 10 человек.  То есть цена указана за всю яхту.  Вы можете отправиться в море компанией друзей и весело и беззаботно провести время. Общаться, веселиться, заняться морской рыбалкой, искупаться в открытом море. Такой отдых внесёт разнообразие в  вашу жизнь, подарит незабываемые впечатления и не «ударит по карману» если собраться всей компанией.
</p>
<p>Также можно отправиться на прогулку «узким» кругом самых родных и близких людей. Уединиться от всех, вдохнуть запах солёного моря, насладиться природой, ослепительными видами и наполниться зарядом энергией на долгое время.
</p>
