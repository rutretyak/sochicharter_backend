                            <span class="accordion__question_icon question-bg"></span>
                            Можно ли приготовить улов на борту?
                        </span>
                    </div>
                    <div class="accordion__a">
                        <p>Выход в море – это всегда наполненный положительными эмоциями и будоражащий сознание  отдых. На яхте можно замечательно провести время, расслабиться, вдохнуть чистый морской воздух полной грудью при этом любуясь величием природы и её невероятной красотой. Одним из развлечений во время такой прогулки может стать морская рыбалка! Любой рыбак (опытный или начинающий) не сможет отказать себе в таком удовольствии. Морская рыбалка  может стать небольшим приключением, которое в итоге принесёт либо ощущение умиротворения либо же наполнит каждую клеточку организма адреналином. Конечно же,  самых упорных рыбаков ожидает вознаграждение в виде аппетитного улова, который можно приготовить на борту некоторых яхт. Поэтому, если в ваших планах на морскую прогулку есть рыбалка с дальнейшим приготовление рыбы на борту, то этот вопрос необходимо уточнять при бронировании яхты!
                        </p>
                    </div>
