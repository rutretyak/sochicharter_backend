                    <div class="accordion__a">
<p>Если вы решили воспользоваться нашими услугами, то прекрасное настроение на отдыхе Вам гарантировано! Прогулка в море – это возможность посетить живописные места и уголки природы, куда не всегда можно добраться сухопутными путями, отдохнуть душой и телом от ежедневной суеты, толпы туристов и побыть наедине со своими родными и близкими людьми. Во время прогулки вы можете попробовать морскую рыбалку. Опытные рыбаки знают, что именно морская рыбалка является одной из самых захватывающих. 
</p>
<p class="faq__bold">Доплаты за рыбалку нет, снасти выдаются на яхте.
</p>
<p>Не упустите возможность, арендовав яхту для отдыха, попробовать себя в роли рыбака. Кто знает, может это станет Вашим новым хобби в жизни? Ведь море еще никого не оставило равнодушным. 
</p>
<p>Для опытных рыбаков это развлечение будет сопровождаться богатым уловом, который при желании можно приготовить на борту яхты ( данный вопрос необходимо обсуждать при бронировании, чтоб подобрать для Вас подходящее судно).
</p>
