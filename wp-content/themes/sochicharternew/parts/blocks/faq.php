                        <span>
                            <span class="accordion__question_icon question-bg"></span>
                            Как рассчитать стоимость аренды яхты?
                        </span>
                    </div>
                    <div class="accordion__a">
<p>Стоимость яхты рассчитывается очень легко. Выбрав яхту, вы умножаете ее стоимость за час аренды на количество часов, на которое вы арендуете. Получается сумма за все время морской прогулки. 
</p>
<p>Если же вы пользуетесь дополнительными услугами, такими как кейтеринг, оформление яхты, аниматоры для детей и взрослых, фотосессии на яхте и т.д., то эти все услуги оплачиваются дополнительно.
</p>
<p>Подведя итог можно вывести несложную формулу расчета аренды судна:
</p>
<p>Стоимость аренды яхты за час*количество часов + дополнительный услуги = море эмоций, впечатлений, улыбок и воспоминаний.
</p>
                    </div>
