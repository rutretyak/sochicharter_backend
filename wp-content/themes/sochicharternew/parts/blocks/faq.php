                            Будет ли на яхте капитан?
                        </span>
                    </div>
                    <div class="accordion__a">
<p>При аренде яхты на борту обязательно будет экипаж (капитан либо капитан с матросом)  
</p>
<p>Отправляясь на морскую прогулку, Вы можете забыть  обо всех проблемах и переживаниях, особенно если они каким либо образом были связаны с безопасностью во время отдыха на яхте! 
</p>
<p>Управление судном  всегда находится в руках самого опытного и квалифицированного  экипажа. Каждый член экипажа обладает уникальными личностными качествами. Капитан несёт невероятную ответственность, а также багаж знаний и навыков за плечами, благодаря которым, доверив свой отдых нам, вы можете быть абсолютно спокойны! 
</p>
<p>Отпустите все свои  мысли, оставьте проблемы, невзгоды и переживания на берегу! 
</p>
<p>Отправляясь в море, Вы погрузитесь в ощущения гармонии, спокойствия и невероятного счастья!
</p>
<p>А мы, в свою очередь, с делаем всё, чтоб Ваш отдых был максимально комфортным и безопасным. 
</p>
