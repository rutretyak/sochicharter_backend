                    <div class="accordion__q">
                        <span>
                            <span class="accordion__question_icon question-bg"></span>
                            Стоимость яхты из чего состоит?
                        </span>
                    </div>
                    <div class="accordion__a">
<p>Расчитывая во сколько обойдётся аренды яхты, Вы сталкиваетесь с таким понятием , как стоимость яхты. Конечно же возникает вопрос: что же входит в эту стоимость яхты? За что мы платим?
</p>
<p>Стоимость яхты включает в себя во-первых само судно. Оно предоставлено будет только Вам на время аренды. На борту будете только Вы и экипаж. Наличие экипажа также входит в стоимость яхты. Вам не придётся беспокоиться о том, кто будет управлять и нужны ли для аренды яхты  права. Экипаж – это капитан либо капитан с матросом (зависит от вида яхты). Последнее, что входит в стоимость яхты – это топливо из расчёта круизной скорости.  Если же Вы планируете во время выхода в море как говориться «прокатиться с ветерком», то это топливо будет уже относиться к дополнительным расходам.
</p>
