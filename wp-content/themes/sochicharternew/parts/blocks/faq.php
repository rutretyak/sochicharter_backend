                            <span class="accordion__question_icon question-bg"></span>
                            Какое минимальное время аренды яхты?
                        </span>
                    </div>
                    <div class="accordion__a">
<p>Время прогулки на яхте целиком и полностью зависит от Вас самих, а точнее от целей , которые Вы преследуете, арендуя яхту. 
</p>
<p>Минимальное время аренды яхты составляет 1 час. Для яхт более 44 футов минимальное время аренды составляет 2 часа.
</p>
<p>Арендуя яхту, всегда учитывайте, что вы хотите получить от прогулки. Если же одной из Ваших целей является сменить стереотип, что на море смотрят с берега и посмотреть на достопримечательности, пляжи, береговую линию со стороны моря, то конечно можно ограничиться минимальным временем аренды. Однако помните, что время на яхте пролетает не просто быстро, а молниеносно….только вы ощутите полную свободу, привкус соленой воды на губах и наслаждение от свежего морского воздуха, как настанет время отправляться на берег. 
</p>
<p>Поэтому, для того чтоб полноценно отдохнуть и успеть насладиться морским побережьем, мы советуем арендовать яхту от 3 часов. Если же вы планируете проведение какого либо мероприятия на яхте, то время потребуется значительно больше.
</p>
